// struktur hierarki DOM

// Root Node
//  - node yang menjadi sumber dari semua node lain di dalam DOM 
//  - document

// Parent Node 
//  - node yang berada 1 tingkat diatas node yang lain  
//  - body adalah parent dari h1, p, p, div dan a

// Child Node
//  - node yang berada 1 tingkat dibawah node yang lain
//  - h1 adalah child dari body

// DOM Selection Method
//  - getElementById() {memilih element yang ada didalam DOM berdasarkan ID-nya} hanya da 1
//      ~ menghasilkan element

//  - getElementsByTagName() {mencari element-element yang memiliki nama tag tertentu} mungkin saja banyak
//      ~ menghasilkan HTMLCollection

//  - getElementsByClassName() {mencari element yang memiliki nama kelas tertentu} lebih dari 1 element
//      ~ menghasilkan HTMLCollection

//  - querySelector() {mencari element berdasarkan selectornya}
//      ~ menghasilkan element

